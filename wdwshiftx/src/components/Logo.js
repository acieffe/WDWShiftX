var d = new Date(); // Today's Date
const year = d.getFullYear(); // This Year

// Function to find out if today is the last Monday of May
// aka Memorial Day
function isMemorial(d) {
	if (d.getMonth() + 1 !== 5 || d.getDay() !== 1) {
		return false;
	} else if (
		d.getDate() === 25 ||
		d.getDate() === 26 ||
		d.getDate() === 27 ||
		d.getDate() === 28 ||
		d.getDate() === 28 ||
		d.getDate() === 30 ||
		d.getDate() === 31
	) {
		return true;
	}
}

// Finds out if today is the fourth Thursday of November
// aka Thanksgiving Day
if (
	d.getMonth() + 1 === 11 &&
	d.getDay() !== 5 &&
	(d.getDate() === 22 ||
		d.getDate() === 23 ||
		d.getDate() === 24 ||
		d.getDate() === 25 ||
		d.getDate() === 26 ||
		d.getDate() === 27 ||
		d.getDate() === 28)
) {
	thanksgiving();
}

// Christmas Time Call
const xmasStart = new Date(`12/1/${year} 00:00:00 AM`); //Christmas Start
const xmasEnd = new Date(`12/26/${year} 11:59:59 PM`); // Christmas End
if (d >= xmasStart && d <= xmasEnd) {
	changeXMas();
}

// Earth Day Call
const earth = new Date(`4/22/${year}`);
if (d.getMonth() === earth.getMonth() && d.getDate() === earth.getDate()) {
	earthDay();
}

// Fourth of July, Memorial Day & Patriot Day Calls
const fourth = new Date(`7/4/${year}`); // Fourth of July
const patriot = new Date(`9/11/${year}`); // 9/11 (Patriot Day)
if (
	(d.getMonth() === fourth.getMonth() && d.getDate() === fourth.getDate()) ||
	(d.getMonth() === patriot.getMonth() && d.getDate() === patriot.getDate()) ||
	isMemorial(d)
) {
	fourthJuly();
}

// Valentine's Day Call
const valentine = new Date(`2/14/${year}`);
if (d.getMonth() === valentine.getMonth() && d.getDate() === valentine.getDate()) {
	valentines();
}

// St. Pattrick's Day Call
const stPatrick = new Date(`3/17/${year}`);
if (d.getMonth() === stPatrick.getMonth() && d.getDate() === stPatrick.getDate()) {
	stPatty();
}

// Halloween Call
const halloweenDay = new Date(`10/31/${year}`);
if (d.getMonth() === halloweenDay.getMonth() && d.getDate() === halloweenDay.getDate()) {
	halloween();
}

// Mickey's Birthday
const mickeyBDay = new Date(`11/18/${year}`);
if (d.getMonth() === mickeyBDay.getMonth() && d.getDate() === mickeyBDay.getDate()) {
	mickey();
}
